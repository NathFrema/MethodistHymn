package com.admin.methodisthymn.model;

public class ItemLala {
    private String id;
    private String lala;
    private String stanza;
    private String summary;
    private String cotents;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLala() {
        return lala;
    }

    public void setLala(String lala) {
        this.lala = lala;
    }

    public String getStanza() {
        return stanza;
    }

    public void setStanza(String stanza) {
        this.stanza = stanza;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public String getCotents() {
        return cotents;
    }

    public void setCotents(String cotents) {
        this.cotents = cotents;
    }
}
